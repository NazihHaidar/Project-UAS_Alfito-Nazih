    private void autonumber(){
        try{
            java.sql.Connection conn=(Connection)koneksi.ambilKoneksiDatabase();
            java.sql.Statement stm = conn.createStatement();
            String sql = "SELECT * FROM login ORDER BY id DESC";
            java.sql.ResultSet res= stm.executeQuery(sql);
            if(res.next()){
                String NoID = res.getString("id").substring(2);
                String ID = "" +(Integer.parseInt(NoID)+1);
                String Zero= "";
                
                if (ID.length()==1)
                {Zero = "00";}
                else if (ID.length()==2)
                {Zero = "0";}
                else if(ID.length()==3)
                {Zero = "";}
                
                txtid.setText("AD" + Zero + ID);
            }else{
                txtid.setText("ADMIN01");
            }
            res.close();
            stm.close();
            }catch(SQLException e){
            System.out.println("Autonumber error");
        }
    }

