    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed
        // TODO add your handling code here:
        String id= txtid.getText().toString().trim();
        String username= txtusername.getText().toString().trim();
        String password= txtpassword.getText().toString().trim();
        String konfirmasipassword= txtkonfirmasipassword.getText().toString().trim();
        
        if (!password.equals(konfirmasipassword)){
            JOptionPane.showMessageDialog(null, "Kata sandi tidak cocok");
        }else if(password.equals("") || username.equals("")){
            JOptionPane.showMessageDialog(null, "Nama pengguna dan kata sandi tidak boleh kosong");
        }else{
            try {
                java.sql.Connection conn=(Connection)koneksi.ambilKoneksiDatabase();
                String sql = "INSERT INTO login VALUES(?, ?, ?)";
                java.sql.Statement p =conn.prepareStatement(sql);
                p.setString(1, id);
                p.setString(2, username);
                p.setString(3, password);
                p.executeUpdate();
                JOptionPane.showMessageDialog(null, "Sukses Buat Akun");
            }catch(SQLException e){
                System.out.println("Error");
            }finally{
                this.dispose();
                Login l = new Login();
                l.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnregisterActionPerformed

